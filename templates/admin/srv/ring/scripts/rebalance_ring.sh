#!/bin/bash

RINGBUILDER="/usr/bin/swift-ring-builder"
TIMER="/usr/bin/time"

OBJ="object.builder"
CON="container.builder"
ACC="account.builder"


printf "\n Rebalancing Object Ring: \n"
$TIMER $RINGBUILDER $OBJ rebalance 

printf "\n Rebalancing Container Ring: \n"
$TIMER $RINGBUILDER $CON rebalance 

printf "\n Rebalancing Account Ring: \n"
$TIMER $RINGBUILDER $ACC rebalance 

printf "\n"

